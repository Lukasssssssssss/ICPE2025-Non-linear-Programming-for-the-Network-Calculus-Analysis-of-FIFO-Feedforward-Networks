package org.networkcalculus.dnc.optree.symbolic;

import nilgiri.math.DoubleReal;
import nilgiri.math.autodiff.DifferentialFunction;

/**
 * Represents the delay bound term in symbolic notation in an OpTreeAnalysis (a DNC analysis using a binary operator tree).
 *
 * @author Lukas Herll
 */

public class DelayTermSymbolic implements SymbolicTerm{
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //attributes
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    private DifferentialFunction<DoubleReal> term;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //constructors
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public DelayTermSymbolic(DifferentialFunction<DoubleReal> term){
        this.term = term;
    }


    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //methods
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    @Override
    public DifferentialFunction<DoubleReal> getTerm() {
        return this.term;
    }


    @Override
    public String toString(){
        return String.format("h = %s", term);
    }
}
